// Нормалізація для пошуку без врахування регістру/пробілів
function norm(s) {
  return String(s || '').toLowerCase().replace(/[\s\-’'`]/g, '')
}

// Пошук НП за початком назви (спочатку), потім за входженням
export function searchSettlements(query, limit = 8) {
  const q = norm(query)
  if (q.length < 1) return []
  const starts = []
  const contains = []
  for (const name of SETTLEMENTS) {
    const n = norm(name)
    if (n.startsWith(q)) starts.push(name)
    else if (n.includes(q)) contains.push(name)
  }
  return [...starts, ...contains].slice(0, limit).map((name) => ({
    label: name,
    sub: 'Одеська область',
    data: { name },
  }))
}
